<?php
//while   // estrura de repetição
$x = 0;
while ($x <= 10) {
    echo "<br>Numero$x";
    $x++;
}
//for
for ($x = 0; $x <= 10; $x++) {
    echo "<br>Numero$x";
}

//Do while
$x = 0;
do{
    echo "<br>Numero$x";
    $x++;
  
} while ($x <= 10);

?>

